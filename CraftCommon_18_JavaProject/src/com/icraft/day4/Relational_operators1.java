package com.icraft.day4;

public class Relational_operators1 {

	public static void main(String as[]) {
		int a, b;
		a = 40;
		b = 40;
		System.out.println("a == b = " + (a == b)); //true
		System.out.println("a != b = " + (a != b)); //false
		System.out.println("a > b = " + (a > b)); //false
		System.out.println("a < b = " + (a < b)); //false
		System.out.println("b >= a = " + (b >= a)); //true
		System.out.println("b <= a = " + (b <= a)); //true
	}
}
